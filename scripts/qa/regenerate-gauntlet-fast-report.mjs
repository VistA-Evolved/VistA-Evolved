#!/usr/bin/env node
// Phase 574 (PromptFolder: 574-PHASE-574-QA-FAST-TRUTH-REPORT)
// Deterministic generator: runs FAST gauntlet, writes docs/QA_GAUNTLET_FAST_RESULTS.md
// No invented results -- report is always generated from an actual gauntlet run.

import { execSync, execFileSync } from 'node:child_process';
import { writeFileSync, readFileSync, existsSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ROOT = resolve(__dirname, '..', '..');
const REPORT_PATH = resolve(ROOT, 'docs', 'QA_GAUNTLET_FAST_RESULTS.md');
const ARTIFACT_PATH = resolve(ROOT, 'artifacts', 'qa-gauntlet.json');

// ── Step 1: Get git SHA ─────────────────────────────────────
let gitSha = 'unknown';
try {
  gitSha = execSync('git rev-parse --short HEAD', { cwd: ROOT, encoding: 'utf-8' }).trim();
} catch {
  console.warn('WARN: Could not get git SHA, using "unknown"');
}

// ── Step 2: Run gauntlet FAST suite ─────────────────────────
console.log('Running QA Gauntlet FAST suite...');
let jsonOutput;
let exitCode = 0;

try {
  const stdout = execFileSync(
    process.execPath,
    [resolve(ROOT, 'qa', 'gauntlet', 'cli.mjs'), '--suite', 'fast', '--ci'],
    { cwd: ROOT, encoding: 'utf-8', timeout: 300_000, stdio: ['ignore', 'pipe', 'pipe'] }
  );
  jsonOutput = JSON.parse(stdout.trim());
} catch (err) {
  // execFileSync throws if exit code != 0 (gauntlet has failures)
  // but stdout still contains the JSON
  if (err.stdout) {
    try {
      jsonOutput = JSON.parse(err.stdout.trim());
      exitCode = err.status || 1;
    } catch {
      // stdout wasn't valid JSON
    }
  }
  if (!jsonOutput) {
    // Fallback: read the artifact file the gauntlet always writes
    if (existsSync(ARTIFACT_PATH)) {
      console.warn('WARN: CLI JSON capture failed, reading artifacts/qa-gauntlet.json');
      const raw = readFileSync(ARTIFACT_PATH, 'utf-8');
      jsonOutput = JSON.parse(raw.charCodeAt(0) === 0xfeff ? raw.slice(1) : raw);
      exitCode = err.status || 1;
    } else {
      console.error('ERROR: Gauntlet run failed and no artifact found. Cannot generate report.');
      console.error(err.message || err);
      process.exit(2);
    }
  }
}

console.log(
  `Gauntlet finished: ${jsonOutput.totals.pass} PASS, ${jsonOutput.totals.fail} FAIL, ${jsonOutput.totals.warn} WARN, ${jsonOutput.totals.skip} SKIP`
);

// ── Step 3: Build markdown ──────────────────────────────────
const { totals, gates, durationMs, generatedAt, suite } = jsonOutput;
const totalGates = gates.length;
const durationSec = (durationMs / 1000).toFixed(1);
const timestamp = generatedAt || new Date().toISOString();

function statusEmoji(s) {
  switch (s) {
    case 'pass':
      return 'PASS';
    case 'fail':
      return '**FAIL**';
    case 'warn':
      return 'WARN';
    case 'skip':
      return 'SKIP';
    default:
      return s.toUpperCase();
  }
}

function overallVerdict() {
  if (totals.fail > 0) return 'FAIL';
  if (totals.warn > 0) return 'PASS (with warnings)';
  return 'PASS';
}

let md = '';

// Header
md += `# QA Gauntlet FAST Results\n\n`;
md += `> **Auto-generated by \`scripts/qa/regenerate-gauntlet-fast-report.mjs\`**\n`;
md += `> Do NOT edit this file manually -- regenerate with \`pnpm qa:report:fast\`\n\n`;
md += `| Field | Value |\n`;
md += `|-------|-------|\n`;
md += `| Generated | ${timestamp} |\n`;
md += `| Commit | \`${gitSha}\` |\n`;
md += `| Suite | ${suite} |\n`;
md += `| Duration | ${durationSec}s |\n`;
md += `| Verdict | **${overallVerdict()}** |\n\n`;

// Summary table
md += `## Summary\n\n`;
md += `| Status | Count |\n`;
md += `|--------|-------|\n`;
md += `| PASS | ${totals.pass} |\n`;
md += `| FAIL | ${totals.fail} |\n`;
md += `| WARN | ${totals.warn} |\n`;
md += `| SKIP | ${totals.skip} |\n`;
md += `| **Total** | **${totalGates}** |\n\n`;

// Gate table
md += `## Gate Results\n\n`;
md += `| # | Gate | Status | Duration |\n`;
md += `|---|------|--------|----------|\n`;
for (let i = 0; i < gates.length; i++) {
  const g = gates[i];
  const dur = g.durationMs != null ? `${g.durationMs}ms` : '-';
  md += `| ${i + 1} | ${g.name} | ${statusEmoji(g.status)} | ${dur} |\n`;
}
md += `\n`;

// Per-gate detail sections
md += `## Gate Details\n\n`;
for (const g of gates) {
  md += `### ${g.name} (${g.id}) -- ${statusEmoji(g.status)}\n\n`;
  if (g.details && g.details.length > 0) {
    md += `| # | Sub-check |\n`;
    md += `|---|----------|\n`;
    for (let i = 0; i < g.details.length; i++) {
      md += `| ${i + 1} | ${g.details[i]} |\n`;
    }
  } else {
    md += `No detail lines reported.\n`;
  }
  md += `\n`;
}

// Footer
md += `---\n\n`;
md += `## Raw Artifacts\n\n`;
md += `- Machine output: \`artifacts/qa-gauntlet.json\`\n`;
md += `- Run timestamp: ${timestamp}\n`;
md += `- Git SHA: \`${gitSha}\`\n`;

// ── Step 4: Write report ────────────────────────────────────
writeFileSync(REPORT_PATH, md, 'utf-8');
console.log(`Report written: docs/QA_GAUNTLET_FAST_RESULTS.md`);
console.log(`Verdict: ${overallVerdict()}`);

// Mirror gauntlet exit code (non-zero if any FAIL)
setTimeout(() => process.exit(exitCode), 50);
