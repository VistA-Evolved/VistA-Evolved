#!/usr/bin/env node
import { readdirSync, readFileSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const routeDir = join(ROOT, 'apps', 'api', 'src', 'routes', 'vista');

const files = readdirSync(routeDir)
  .filter(f => f.endsWith('.ts') && f !== 'index.ts')
  .sort();

console.log(`Found ${files.length} route files`);

const imports = [];
const registers = [];

for (const f of files) {
  const ns = f.replace('.ts', '');
  const content = readFileSync(join(routeDir, f), 'utf-8');
  const fnMatch = content.match(/export\s+async\s+function\s+(\w+)/);
  const fnName = fnMatch ? fnMatch[1] : `${ns}Routes`;
  imports.push(`import { ${fnName} } from './${ns}.js';`);
  registers.push(`  await server.register(${fnName});`);
}

const out = `// Auto-generated barrel for all VistA module routes
// Generated by vista-module-gen: ${new Date().toISOString().slice(0, 10)}
// ${files.length} packages from Vivian + WorldVistA index

import type { FastifyInstance } from 'fastify';

${imports.join('\n')}

export async function registerVistaModuleRoutes(server: FastifyInstance): Promise<void> {
${registers.join('\n')}
}
`;

writeFileSync(join(routeDir, 'index.ts'), out);
console.log(`Wrote index.ts with ${files.length} route imports`);
