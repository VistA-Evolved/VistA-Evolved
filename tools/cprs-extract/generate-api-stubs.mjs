#!/usr/bin/env node
/**
 * generate-api-stubs.mjs — Scaffold generator for typed API endpoint stubs.
 *
 * Reads rpc_catalog.json + screen_registry.json and generates one Fastify
 * route plugin per clinical domain under apps/api/src/routes/.
 *
 * Each RPC gets a stub endpoint that returns { ok: false, error: "Not implemented" }
 * with the correct rpcName so future wiring only needs to fill in the call logic.
 *
 * Usage:  node tools/cprs-extract/generate-api-stubs.mjs
 */

import { readFile, writeFile, mkdir } from 'node:fs/promises';
import { join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = fileURLToPath(new URL('.', import.meta.url));
const ROOT = resolve(__dirname, '..', '..');

const CATALOG_PATH = join(ROOT, 'design', 'contracts', 'cprs', 'v1', 'rpc_catalog.json');
const REGISTRY_PATH = join(ROOT, 'design', 'contracts', 'cprs', 'v1', 'screen_registry.json');
const ROUTES_DIR = join(ROOT, 'apps', 'api', 'src', 'routes');

// ── Domain classification by RPC name prefix ─────────────────────────
const DOMAIN_RULES = [
  { domain: 'problems', prefixes: ['ORQQPL'] },
  { domain: 'meds',     prefixes: ['ORWPS', 'ORWDPS'] },
  { domain: 'notes',    prefixes: ['TIU', 'ORWTIU'] },
  { domain: 'orders',   prefixes: ['ORWDX', 'ORWOR', 'ORWORR'] },
  { domain: 'labs',     prefixes: ['ORWLRR', 'ORWDLR'] },
  { domain: 'reports',  prefixes: ['ORWRP', 'ORWSR'] },
];

/** Classify a single RPC name into a domain (or null if unclassified). */
function classifyRpc(rpcName) {
  for (const rule of DOMAIN_RULES) {
    for (const prefix of rule.prefixes) {
      if (rpcName.startsWith(prefix)) return rule.domain;
    }
  }
  return null;
}

/** Turn an RPC name into a URL-safe slug: "ORQQPL PROBLEM LIST" → "orqqpl-problem-list" */
function rpcSlug(name) {
  return name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
}

/** Turn an RPC name into a TypeScript-safe identifier: "ORQQPL PROBLEM LIST" → "orqqplProblemList" */
function rpcIdent(name) {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, ' ')
    .trim()
    .split(' ')
    .map((w, i) => (i === 0 ? w : w[0].toUpperCase() + w.slice(1)))
    .join('');
}

// ── Generate a single domain route file ──────────────────────────────
function generateDomainFile(domain, rpcs) {
  const lines = [];

  lines.push(`/**`);
  lines.push(` * routes/${domain}.ts — Auto-generated RPC stubs for the "${domain}" domain.`);
  lines.push(` *`);
  lines.push(` * Generated by tools/cprs-extract/generate-api-stubs.mjs`);
  lines.push(` * DO NOT EDIT BY HAND — re-run the generator to update.`);
  lines.push(` *`);
  lines.push(` * Each endpoint returns { ok: false, error: "Not implemented" } until wired.`);
  lines.push(` * To implement: replace the stub body with connect() → callRpc() → disconnect().`);
  lines.push(` */`);
  lines.push(``);
  lines.push(`import type { FastifyInstance } from "fastify";`);
  lines.push(``);
  lines.push(`/** RPC catalog entry for the ${domain} domain. */`);
  lines.push(`export interface ${capitalize(domain)}Rpc {`);
  lines.push(`  name: string;`);
  lines.push(`  slug: string;`);
  lines.push(`  isContext: boolean;`);
  lines.push(`  referenceCount: number;`);
  lines.push(`}`);
  lines.push(``);
  lines.push(`/** All RPCs in the ${domain} domain. */`);
  lines.push(`export const ${domain.toUpperCase()}_RPCS: ${capitalize(domain)}Rpc[] = [`);
  for (const rpc of rpcs) {
    lines.push(`  { name: ${JSON.stringify(rpc.name)}, slug: ${JSON.stringify(rpc.slug)}, isContext: ${rpc.isContext}, referenceCount: ${rpc.referenceCount} },`);
  }
  lines.push(`];`);
  lines.push(``);

  // Stub response type
  lines.push(`/** Stub response returned by unimplemented endpoints. */`);
  lines.push(`export interface StubResponse {`);
  lines.push(`  ok: false;`);
  lines.push(`  error: "Not implemented";`);
  lines.push(`  rpcName: string;`);
  lines.push(`  domain: string;`);
  lines.push(`  slug: string;`);
  lines.push(`  hint: string;`);
  lines.push(`}`);
  lines.push(``);

  // Fastify plugin
  lines.push(`/**`);
  lines.push(` * Register all ${domain} RPC stub routes.`);
  lines.push(` *`);
  lines.push(` * Routes: GET /vista/${domain}/rpc/:slug`);
  lines.push(` *         GET /vista/${domain}/rpcs  (catalog listing)`);
  lines.push(` */`);
  lines.push(`export default async function ${domain}Routes(server: FastifyInstance): Promise<void> {`);
  lines.push(``);
  lines.push(`  // ── Catalog listing ───────────────────────────────────────────`);
  lines.push(`  server.get("/vista/${domain}/rpcs", async () => {`);
  lines.push(`    return {`);
  lines.push(`      ok: true,`);
  lines.push(`      domain: "${domain}",`);
  lines.push(`      count: ${rpcs.length},`);
  lines.push(`      rpcs: ${domain.toUpperCase()}_RPCS,`);
  lines.push(`    };`);
  lines.push(`  });`);
  lines.push(``);

  // Individual RPC stub endpoints
  lines.push(`  // ── Individual RPC stubs ──────────────────────────────────────`);
  for (const rpc of rpcs) {
    lines.push(``);
    lines.push(`  /** Stub for ${rpc.name} (${rpc.referenceCount} call site${rpc.referenceCount !== 1 ? 's' : ''} in CPRS) */`);
    lines.push(`  server.get("/vista/${domain}/rpc/${rpc.slug}", async (): Promise<StubResponse> => {`);
    lines.push(`    return {`);
    lines.push(`      ok: false,`);
    lines.push(`      error: "Not implemented",`);
    lines.push(`      rpcName: ${JSON.stringify(rpc.name)},`);
    lines.push(`      domain: "${domain}",`);
    lines.push(`      slug: "${rpc.slug}",`);
    lines.push(`      hint: "Wire this stub: import { connect, callRpc, disconnect } from '../vista/rpcBrokerClient'",`);
    lines.push(`    };`);
    lines.push(`  });`);
  }

  lines.push(`}`);
  lines.push(``);

  return lines.join('\n');
}

/** Capitalize first letter. */
function capitalize(s) {
  return s[0].toUpperCase() + s.slice(1);
}

// ── Generate the barrel index file ───────────────────────────────────
function generateIndexFile(domains) {
  const lines = [];
  lines.push(`/**`);
  lines.push(` * routes/index.ts — Barrel import for all domain route plugins.`);
  lines.push(` *`);
  lines.push(` * Generated by tools/cprs-extract/generate-api-stubs.mjs`);
  lines.push(` * DO NOT EDIT BY HAND — re-run the generator to update.`);
  lines.push(` */`);
  lines.push(``);
  lines.push(`import type { FastifyInstance } from "fastify";`);
  lines.push(``);

  for (const d of domains) {
    lines.push(`import ${d}Routes from "./${d}.js";`);
  }
  lines.push(``);

  lines.push(`/** Register all domain route plugins on the server. */`);
  lines.push(`export async function registerDomainRoutes(server: FastifyInstance): Promise<void> {`);
  for (const d of domains) {
    lines.push(`  await server.register(${d}Routes);`);
  }
  lines.push(`}`);
  lines.push(``);

  // Also export the domain list for introspection
  lines.push(`/** All scaffolded domains. */`);
  lines.push(`export const SCAFFOLDED_DOMAINS = ${JSON.stringify(domains)} as const;`);
  lines.push(``);

  return lines.join('\n');
}

// ── Main ─────────────────────────────────────────────────────────────
async function main() {
  console.log('Reading RPC catalog…');
  const catalog = JSON.parse(await readFile(CATALOG_PATH, 'utf-8'));

  // Classify every RPC into a domain
  /** @type {Record<string, Array<{name:string, slug:string, isContext:boolean, referenceCount:number}>>} */
  const domainMap = {};
  for (const rule of DOMAIN_RULES) {
    domainMap[rule.domain] = [];
  }

  let classified = 0;
  for (const rpc of catalog.rpcs) {
    const domain = classifyRpc(rpc.name);
    if (!domain) continue;
    // Skip context RPCs (XUS SIGNON, OR CPRS GUI CHART) — not callable endpoints
    if (rpc.isContext) continue;
    domainMap[domain].push({
      name: rpc.name,
      slug: rpcSlug(rpc.name),
      isContext: rpc.isContext,
      referenceCount: rpc.references?.length || 0,
    });
    classified++;
  }

  // Sort each domain alphabetically
  for (const domain of Object.keys(domainMap)) {
    domainMap[domain].sort((a, b) => a.name.localeCompare(b.name));
  }

  console.log(`Classified ${classified} RPCs across ${Object.keys(domainMap).length} domains:`);
  for (const [domain, rpcs] of Object.entries(domainMap)) {
    console.log(`  ${domain}: ${rpcs.length} RPCs`);
  }

  // Create output directory
  await mkdir(ROUTES_DIR, { recursive: true });

  // Generate domain files
  const domains = Object.keys(domainMap);
  for (const domain of domains) {
    const content = generateDomainFile(domain, domainMap[domain]);
    const outPath = join(ROUTES_DIR, `${domain}.ts`);
    await writeFile(outPath, content, 'utf-8');
    console.log(`  ✓ ${outPath.replace(ROOT + '/', '').replace(ROOT + '\\', '')}`);
  }

  // Generate barrel index
  const indexContent = generateIndexFile(domains);
  const indexPath = join(ROUTES_DIR, 'index.ts');
  await writeFile(indexPath, indexContent, 'utf-8');
  console.log(`  ✓ ${indexPath.replace(ROOT + '/', '').replace(ROOT + '\\', '')}`);

  console.log(`\nDone — ${domains.length} domain route files + index generated.`);
}

main().catch((err) => {
  console.error('ERROR:', err);
  process.exit(1);
});
