ZVETIUD ; VEHU TIU debug probe ; 2026-03-08
 Q
 ; Usage:
 ;   S IEN=14357 D DUMP^ZVETIUD
 ;   or
 ;   D DUMP^ZVETIUD(14357)
RUN ; Run using ^XTMP("ZVETIUD","IEN")
 D DUMP($G(^XTMP("ZVETIUD","IEN")))
 Q
TEST ; Convenience entry for current shell-note investigation
 D DUMP(14357)
 Q
DUMP(IEN) ; Dump key TIU globals for a note IEN
 N TIUDA,IDX,CHILD,ZERO,Z12,Z13,Z15,SUB,ERRDT,CNT
 S TIUDA=+$G(IEN)
 I 'TIUDA S TIUDA=+$G(^TMP("ZVETIUD",$J,"IEN"))
 I 'TIUDA S TIUDA=+$G(IEN)
 I 'TIUDA W !,"ERR: missing IEN" Q
 I '$D(^TIU(8925,TIUDA,0)) W !,"ERR: note not found: ",TIUDA Q
 S ZERO=$G(^TIU(8925,TIUDA,0))
 S Z12=$G(^TIU(8925,TIUDA,12))
 S Z13=$G(^TIU(8925,TIUDA,13))
 S Z15=$G(^TIU(8925,TIUDA,15))
 W !,"IEN:",TIUDA
 W !,"0:",ZERO
 W !,"12:",Z12
 W !,"13:",Z13
 W !,"15:",Z15
 W !,"HAS TEMP:",$S($D(^TIU(8925,TIUDA,"TEMP")):1,1:0)
 W !,"TEMP0:",$G(^TIU(8925,TIUDA,"TEMP",0))
 S IDX=0
 F  S IDX=$O(^TIU(8925,TIUDA,"TEMP",IDX)) Q:'+IDX  D
 . W !,"TEMP(",IDX,")=",$G(^TIU(8925,TIUDA,"TEMP",IDX,0))
 W !,"HAS TEXT:",$S($D(^TIU(8925,TIUDA,"TEXT")):1,1:0)
 W !,"TEXT0:",$G(^TIU(8925,TIUDA,"TEXT",0))
 S IDX=0
 F  S IDX=$O(^TIU(8925,TIUDA,"TEXT",IDX)) Q:'+IDX  D
 . W !,"TEXT(",IDX,")=",$G(^TIU(8925,TIUDA,"TEXT",IDX,0))
 W !,"FIRST-LEVEL SUBSCRIPTS:"
 S SUB=""
 F  S SUB=$O(^TIU(8925,TIUDA,SUB)) Q:SUB=""  D
 . W !,"SUB=",SUB
 . I $D(^TIU(8925,TIUDA,SUB,0)) W "  0=",$G(^TIU(8925,TIUDA,SUB,0))
 S CHILD=0
 F  S CHILD=$O(^TIU(8925,"DAD",TIUDA,CHILD)) Q:'+CHILD  D
 . W !,"CHILD:",CHILD,"  TEXT0=",$G(^TIU(8925,CHILD,"TEXT",0))
 . S IDX=0
 . F  S IDX=$O(^TIU(8925,CHILD,"TEXT",IDX)) Q:'+IDX  D
 . . W !,"CHILDTEXT(",CHILD,",",IDX,")=",$G(^TIU(8925,CHILD,"TEXT",IDX,0))
 W !,"TIUERR COUNT:",$G(^XTMP("TIUERR","COUNT"))
 S CNT=0,ERRDT=""
 F  S ERRDT=$O(^XTMP("TIUERR",ERRDT),-1) Q:ERRDT=""!(CNT>4)  D
 . I ERRDT=0!(ERRDT="COUNT") Q
 . W !,"TIUERR DT:",ERRDT
 . W !,"  ECODE:",$G(^XTMP("TIUERR",ERRDT,"ECODE"))
 . W !,"  USER:",$G(^XTMP("TIUERR",ERRDT,"USER"))
 . W !,"  TIUDA:",$G(^XTMP("TIUERR",ERRDT,"TIUDA"))
 . W !,"  TIUHDR:",$G(^XTMP("TIUERR",ERRDT,"TIUHDR"))
 . W !,"  XWBHDR:",$G(^XTMP("TIUERR",ERRDT,"XWBHDR"))
 . S CNT=CNT+1
 Q